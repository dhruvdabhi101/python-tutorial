# division is decimal by default
print(5/2)
 
# double slash round down number
print(5 // 2)

# mod is similar 
# mod to negative number is different
print(-10%3)

# to be consistant with other language 
import math 
print(math.fmod(-10,3))

# more helpers
print(math.floor(3/2))
print(math.ciel(3/2))
print(math.sqrt(2))
print(math.pow(2,2))
